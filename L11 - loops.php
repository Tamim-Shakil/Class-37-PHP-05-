<?php
// start point
// increment
// end point

// while loop
$startPoint = 0;
$endPoint = 9;

while ($startPoint <= $endPoint) {
    echo $startPoint . "<br>";
    $startPoint = $startPoint + 1;
}

echo "<br>";


// for loop
for ($i = 0; $i <= 9; $i++) {
    echo $i . "<br>";
}

echo "<br>";


// do while loop
$x = 20;
do {
    echo $x . "<br>";
    $x++;
} while ($x <= 30);
